#include <stdio.h>
#include <math.h>

int main () {

    // Raiz quadrada de 12
    printf("%.1f\n", sqrt(12)); 

    // Arredondando valores
    printf("%.1f\n", ceil(1.6)); // 2.0
    printf("%.1f\n", floor(1.6)); // 1.0

    // O valor de x elevado a y
    printf("%f\n", pow(4, 2)); // 16

    return 0;
}